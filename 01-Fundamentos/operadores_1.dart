main()
{
  // operadores aritimeticos
  int a = 7;
  int b = 3;

  int resultado = a + b;
  print(resultado);
  print(a-b);
  print(a*b);
  print(a/b);
  print(a%b);

  // operadores logicos
  bool ehFragil = true;
  bool ehCaro = false;

  print(ehFragil && ehCaro);
  print(ehFragil || ehCaro);
  print(ehFragil ^ ehCaro);
  print(!ehCaro);
}
